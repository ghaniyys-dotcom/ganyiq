-- db/migrations/003_create_moments.sql
--
-- Stores every ranked moment discovered by the ganyIQ analysis pipeline.
-- Each row represents a single clippable moment within a video: its
-- timestamp boundaries, quality score, viral DNA profile, and human-
-- readable reasoning.
--
-- The moments table is the core product output. When a clipper pastes
-- a YouTube URL and gets back "Top 5 Elite Moments" — those are rows
-- from this table, served through GET /api/analyze/:id.
--
-- ─── Data Model ─────────────────────────────────────────────────────────
--
-- videos  1──N  analyses  1──N  moments
--                     │
--                     └── events
--
-- Each analysis run produces 10-15 moment rows (5 elite + 10 secondary
-- in MVP). Rows are written atomically together with the analysis row
-- inside a single transaction.
--
-- ─── Column Guide ───────────────────────────────────────────────────────
--
-- id                   UUID primary key. Auto-generated. No sequential
--                      guessing. Not exposed to the frontend (analyses
--                      are referenced by analysis_id, and moments are
--                      always returned as a nested array).
--
-- analysis_id          Foreign key → analyses(id) ON DELETE CASCADE.
--                      Deletes all moments when the parent analysis is
--                      removed. This is the most common JOIN target:
--                      frontend result pages always load moments
--                      belonging to one analysis.
--
-- start_time           Start of the clip in seconds from video start.
--                      NUMERIC(10,2) stores up to 9,999,999.99 seconds
--                      (~115 days), more than enough for any YouTube
--                      video. NOT NULL — every moment must have a
--                      defined start.
--
-- end_time             End of the clip in seconds from video start.
--                      Must be strictly greater than start_time (enforced
--                      by the app layer, not a CHECK constraint, because
--                      LLM output occasionally flips them and we correct
--                      it in lib/ranking.ts before storage).
--
-- worth_clipping_score Composite score 0.00-100.00 from the LLM.
--                      NUMERIC(5,2) allows two decimal places for
--                      future sub-score precision. The scoring formula
--                      is defined in the Master Plan Section 10:
--                        hookStrength×0.25 + standaloneClarity×0.20 + ...
--                      In MVP, the LLM outputs this directly (single-
--                      agent mode); in V2, the Worth-Clipping Evaluator
--                      Agent computes it deterministically from 6
--                      sub-dimensions.
--
--                      Score tiers (per MASTER_PLAN_v1):
--                        85-100  → ↑ Elite    (🔥 clip immediately)
--                        70-84   → → Strong   (solid performer)
--                        55-69   → ↓ Moderate (clippable, not priority)
--                        0-54    → ✕ Skip     (not worth effort)
--
-- confidence           LLM's self-assessed certainty:
--                      'high'   — strong signals across all dimensions
--                      'medium' — potential but some ambiguity
--                      'low'    — AI is guessing, human judgment critical
--                      Constrained by CHECK to prevent invalid values.
--                      Stored as VARCHAR(10) rather than a normalized
--                      lookup table (the list is fixed and small).
--
-- dna_tags             Viral DNA attribute profile stored as JSONB array.
--                      Each array has 1-3 tags drawn from the 12-attribute
--                      framework (hookPower, curiosity, controversy,
--                      emotion, humor, storytelling, authority, money,
--                      shock, educational, motivation, relatability).
--                      JSONB was chosen over a normalized many-to-many
--                      table because:
--                        a) The array is always tiny (≤3 items)
--                        b) It's always read together with the moment
--                        c) No standalone queries filter by DNA tag
--                           at MVP scale
--                      Example: '["hookPower","controversy","authority"]'
--
-- reasoning            Human-readable explanation generated by the LLM.
--                      1-3 sentences in natural language. This is the
--                      trust-building output — clippers read this to
--                      understand why the AI picked this moment. The
--                      tone should match a clipper's vocabulary (not
--                      academic).
--
-- transcript_excerpt   The raw transcript text that falls within this
--                      moment's start_time..end_time range. Extracted
--                      deterministically by lib/ranking.ts from the
--                      video's cached JSONB transcript. NOT returned
--                      from the LLM — computed server-side to ensure
--                      byte-for-byte accuracy.
--
--                      Stored to enable:
--                        a) Contextual display alongside the reasoning
--                        b) Future fine-tuning datasets (input = transcript
--                           excerpt, label = score)
--                        c) Debugging — visually verify the LLM scored
--                           the right words
--
-- rank_position        1-based position within the analysis result set.
--                      Assigned by lib/ranking.ts after:
--                        1. Sorting all moments by worth_clipping_score DESC
--                        2. Deduplicating within a 30-second proximity
--                           window (keep the higher-scored one)
--                        3. Assigning tier labels
--                      The frontend uses rank_position for display order,
--                      not the raw sort (in case the client wants custom
--                      ordering later).
--
-- tier                 'elite' or 'secondary', assigned by the ranking
--                      algorithm. In MVP, elite = score ≥ 85 and rank
--                      within top 5; secondary = score 70-84 and rank
--                      within next 10. Constrained by CHECK.
--
-- ─── Indexes ─────────────────────────────────────────────────────────────
--
-- idx_moments_analysis_id   (B-tree on analysis_id)
--                           Primary query path: every result page loads
--                           moments by analysis_id. Without this index,
--                           every page load would seq-scan the table.
--                           Query:
--                             SELECT * FROM moments
--                             WHERE analysis_id = $1
--                             ORDER BY rank_position ASC;
--
-- idx_moments_score         (B-tree DESC on worth_clipping_score)
--                           Supports the Viral DNA Dataset analysis path:
--                           querying the highest-scoring moments across
--                           all analyses. Also used for the future "top
--                           moments ever" feature and quality monitoring.
--                           Query:
--                             SELECT m.*, v.title
--                             FROM moments m
--                             JOIN analyses a ON a.id = m.analysis_id
--                             JOIN videos v ON v.id = a.video_id
--                             ORDER BY m.worth_clipping_score DESC
--                             LIMIT 50;
--
-- ─── Expected Query Patterns ─────────────────────────────────────────────
--
-- 1. Load all moments for a result page:
--      SELECT * FROM moments
--      WHERE analysis_id = $1
--      ORDER BY rank_position ASC;
--    → Covered by idx_moments_analysis_id.
--
-- 2. Count elite vs secondary per analysis:
--      SELECT tier, COUNT(*)
--      FROM moments
--      WHERE analysis_id = $1
--      GROUP BY tier;
--    → Covered by idx_moments_analysis_id.
--
-- 3. Highest-rated moments across all analyses (dataset mining):
--      SELECT m.worth_clipping_score, m.dna_tags, v.title
--      FROM moments m
--      JOIN analyses a ON a.id = m.analysis_id
--      JOIN videos v ON v.id = a.video_id
--      ORDER BY m.worth_clipping_score DESC
--      LIMIT 100;
--    → Covered by idx_moments_score DESC.
--
-- 4. Moments count summary for dashboard:
--      SELECT COUNT(*) FROM moments;
--    → Sequential scan. Acceptable at MVP scale.
--
-- ─── Storage & Performance Notes ─────────────────────────────────────────
--
-- • EXPECTED SIZE: ~200 bytes per row. 10-15 rows per analysis.
--   At 1000 analyses/week → ~15K rows/week → ~3MB/week.
--   Neon free tier (0.5GB) holds ~6 months of data comfortably.
--
-- • The ON DELETE CASCADE on analysis_id means removing an analysis
--   is a single DELETE FROM analyses WHERE id = $1 — PostgreSQL
--   handles the cascade automatically. No manual cleanup needed.
--
-- • JSONB for dna_tags rather than JSON because JSONB is
--   de-duplicated, faster to query, and supports indexing.
--   The tiny size (3 tags × ~20 chars each) means storage overhead
--   is negligible.


CREATE TABLE IF NOT EXISTS moments (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    analysis_id UUID NOT NULL REFERENCES analyses(id) ON DELETE CASCADE,
    start_time NUMERIC(10,2) NOT NULL,
    end_time NUMERIC(10,2) NOT NULL,
    worth_clipping_score NUMERIC(5,2) NOT NULL,
    confidence VARCHAR(10) NOT NULL
        CHECK (confidence IN ('high', 'medium', 'low')),
    dna_tags JSONB NOT NULL,
    reasoning TEXT,
    transcript_excerpt TEXT,
    rank_position INT,
    tier VARCHAR(10)
        CHECK (tier IN ('elite', 'secondary'))
);

CREATE INDEX IF NOT EXISTS idx_moments_analysis_id ON moments(analysis_id);
CREATE INDEX IF NOT EXISTS idx_moments_score ON moments(worth_clipping_score DESC);
